#ifndef EX1_TO_LOWER_CASE
#define EX1_TO_LOWER_CASE
#include <string>

/** Creates a copy of MixedOrUpperCase but with any upper case characters
 * converted to their corresponding lower case equivalent. characters without a
 * case are ignored.
 *
 *  @param MixedOrUpperCase A string which may contain upper case characters.
 *  @return A copy of MixedOrUpperCase containing only lower case characters.
 */
void toLowerCase(std::string &MixedOrUpperCase);
#endif // EX1_TO_LOWER_CASE
